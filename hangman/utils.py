def initialize_hangman():
    input_path = PurePosixPath(Path.cwd()).joinpath(INPUT)
    words = []
    with open(input_path, 'r', encoding='utf-8') as input_document:
        for word in input_document:
            words.append(word)
    return choice(words).replace("\n", "")


